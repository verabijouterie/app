  constructor(private router: Router) {}

  ngOnInit() {
    // Subscribe to router events to update active state
    this.router.events.subscribe(event => {
      if (event instanceof NavigationEnd) {
        this.updateActiveItem(event.url);
      }
    });

    // Set initial active state
    this.updateActiveItem(this.router.url);
  }

  toggleSidebar() {
    this.isSidebarOpen = !this.isSidebarOpen;
  }

  closeSidebar() {
    this.isSidebarOpen = false;
  }

  selectNavItem(item: NavItem) {
    // Update all items to inactive
    this.navItems.forEach(navItem => {
      navItem.isActive = false;
    });
    
    // Set the selected item as active
    item.isActive = true;
    this.currentActiveItem = item;

    // Navigate to the selected route
    this.router.navigate([item.link]);
  }

  private updateActiveItem(url: string) {
    this.navItems.forEach(item => {
      item.isActive = item.link === url;
    });
    this.currentActiveItem = this.navItems.find(item => item.isActive) || null;
  }
